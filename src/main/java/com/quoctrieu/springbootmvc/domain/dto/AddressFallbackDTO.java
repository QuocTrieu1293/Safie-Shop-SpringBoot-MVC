package com.quoctrieu.springbootmvc.domain.dto;

public class AddressFallbackDTO {
  String id;
  String full_name;

  public AddressFallbackDTO() {
  }

  public AddressFallbackDTO(String id, String full_name) {
    this.id = id;
    this.full_name = full_name;
  }

  public AddressFallbackDTO(String id) {
    this.id = id;
  }

  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }

  public String getFull_name() {
    return full_name;
  }

  public void setFull_name(String full_name) {
    this.full_name = full_name;
  }

  @Override
  public int hashCode() {
    final int prime = 31;
    int result = 1;
    result = prime * result + ((id == null) ? 0 : id.hashCode());
    return result;
  }

  @Override
  public boolean equals(Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    AddressFallbackDTO other = (AddressFallbackDTO) obj;
    if (id == null) {
      if (other.id != null)
        return false;
    } else if (!id.equals(other.id))
      return false;
    return true;
  }

}
